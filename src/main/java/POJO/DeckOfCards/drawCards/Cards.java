package POJO.DeckOfCards.drawCards;

public class Cards {
    String code;
    String image;
    String value;
    String suit;
    Images images;

    public String getCode() {
        return code;
    }

    public String getImage() {
        return image;
    }

    public String getValue() {
        return value;
    }

    public String getSuit() {
        return suit;
    }

    public Images getImages() {
        return images;
    }
}
